package com.protecteddemo;

// INSIDE PACKAGE
// protected member acts as public member, within same package.
// within same package, protected behaves as public (Oneliner)

//OUTSIDE PACKAGE
//protected member acts as private member, outside package.
//outside package, protected acts as private member of the subclass (Oneliner)
//outside package, protected is visible only inside subclass
//outside package, protected acts as private in its subclass


public class A {
	protected int x=10;
	
	
}
